from typing import List, Any


def bubble_sort(arr: List[Any]) -> List[Any]:
    """Standard bubble sort implementation."""
    n = len(arr)
    for i in range(n):
        swapped = False
        for j in range(0, n - i - 1):
            if arr[j] > arr[j + 1]:
                arr[j], arr[j + 1] = arr[j + 1], arr[j]
                swapped = True
        if not swapped:
            break
    return arr


